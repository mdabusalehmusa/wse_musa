//FOr Loop
console.log(" This Is For Loop")
for(var n=0; n<10; n++){
    console.log(n)
}

// while loop
console.log("This is While loop")
var n=10;
while(n<20){
    console.log(n)
    n++
}


// Do while loop
console.log( " This is DO While Loop")

var m=30;
do{
    console.log(m);
    m++
}while(m<40);